public class DataPoint {

    /** the x value */
    public double x;

    /** the y value */
    public double y;

    /**
     * Constructor.
     *
     * @param x
     *            the x value
     * @param y
     *            the y value
     */
    public DataPoint(double x, double y) {
        this.x = x;
        this.y = y;
    }
}
